      SUBROUTINE INIT(X,N,V)
C**********************************************************************C
C INIT.FOR                                                             C
C  02/12-1994   NTH                                                    C
C  Bruno Brouard                                                       C
C**********************************************************************C
C     INITIALIZE VECTOR V TO THE VALUE X
C**********************************************************************C
C  Programmed after Dhatt & Touzot, p.429                              C
C**********************************************************************C
      IMPLICIT REAL*8(A-H,O-Z)
      DIMENSION V(1)
C-----------------------------------------------------------------------
      DO 10 I=1,N      
 10   V(I)=X      
      RETURN
      END    
