      SUBROUTINE ELEMLB(VCORE,VPRNE,VPREE,VDLE,VDLEC,VKE,VKE1,VIE,VIE1,
     1      VFE,VSRE,VNDSRC,KTYPRSG,VPRSG)
C**************************************************************
C     ELEMLB.FOR
C***************************************************************
C     2/1 1995
C     Kompaniet Numerisk Lyd
C     Ulf R. Kristiansen & Tonni F. Johansen
C     New version of ELEMLB to treat both elements with real and complex
C     coefficients
C     Old version of ELEMLB is described in Dhatt og Touzot s.412
C***************************************************************
C     TO COMPUTE ELEMENT INFORMATIONS FOR ALL TYPES OF ELEMENTS
C===============================================================
      include 'femak.cmn'


      REAL*8 VCORE(1),VPRNE(1),VPREE(1),VDLE(1),VKE(1),VKE1(1),VFE(1)
      COMPLEX*16 VIE(1),VIE1(1),VSRE(1),VNDSRC(1),VPRSG(1),VDLEC(1)
      INTEGER KTYPRSG(1)
C---------------------------------------------------------------
C
      IF (ITPE.EQ.1) THEN
        CALL ELEM01(VCORE,VPRNE,VPREE,VDLE,VDLEC,VKE,VKE1,VIE,VIE1,VFE,
     1      VSRE,VNDSRC,KTYPRSG,VPRSG)
      ELSEIF (ITPE.EQ.2) THEN
        CALL ELEM02(VCORE,VPRNE,VPREE,VDLE,VDLEC,VKE,VKE1,VIE,VIE1,VFE,
     1      VSRE,VNDSRC,KTYPRSG,VPRSG)
      ELSEIF (ITPE.EQ.3) THEN
        CALL ELEM03(VCORE,VPRNE,VPREE,VDLE,VDLEC,VKE,VKE1,VIE,VIE1,VFE,
     1      VSRE,VNDSRC,KTYPRSG,VPRSG)
      ELSEIF (ITPE.EQ.4) THEN
        CALL ELEM04(VCORE,VPRNE,VPREE,VDLE,VDLEC,VKE,VKE1,VIE,VIE1,VFE,
     1      VSRE,VNDSRC,KTYPRSG,VPRSG)
      ELSEIF (ITPE.EQ.5) THEN
        CALL ELEM05(VCORE,VPRNE,VPREE,VDLE,VDLEC,VKE,VKE1,VIE,VIE1,VFE,
     1      VSRE,VNDSRC,KTYPRSG,VPRSG)
      ELSEIF (ITPE.EQ.8) THEN
        CALL ELEM08(VCORE,VPRNE,VPREE,VDLE,VDLEC,VKE,VKE1,VIE,VIE1,VFE,
     1      VSRE,VNDSRC,KTYPRSG,VPRSG)
      ELSEIF (ITPE.EQ.9) THEN
        CALL ELEM09(VCORE,VPRNE,VPREE,VDLE,VDLEC,VKE,VKE1,VIE,VIE1,VFE,
     1      VSRE,VNDSRC,KTYPRSG,VPRSG)
      ELSEIF (ITPE.EQ.11) THEN
        CALL ELEM11(VCORE,VPRNE,VPREE,VDLE,VDLEC,VKE,VKE1,VIE,VIE1,VFE,
     1      VSRE,VNDSRC,KTYPRSG,VPRSG)
      ELSEIF (ITPE.EQ.20) THEN
        CALL ELEM20(VCORE,VPRNE,VPREE,VDLE,VDLEC,VKE,VKE1,VIE,VIE1,VFE,
     1      VSRE,VNDSRC,KTYPRSG,VPRSG)
      ELSEIF (ITPE.EQ.91) THEN
        CALL ELEM91(VCORE,VPRNE,VPREE,VDLE,VDLEC,VKE,VKE1,VIE,VIE1,VFE,
     1      VSRE,VNDSRC,KTYPRSG,VPRSG)
      ELSEIF (ITPE.EQ.92) THEN
        CALL ELEM92(VCORE,VPRNE,VPREE,VDLE,VDLEC,VKE,VKE1,VIE,VIE1,VFE,
     1      VSRE,VNDSRC,KTYPRSG,VPRSG)
      ELSEIF (ITPE.EQ.93) THEN
        CALL ELEM93(VCORE,VPRNE,VPREE,VDLE,VDLEC,VKE,VKE1,VIE,VIE1,VFE,
     1      VSRE,VNDSRC,KTYPRSG,VPRSG)
      ELSEIF (ITPE.EQ.94) THEN
        CALL ELEM94(VCORE,VPRNE,VPREE,VDLE,VDLEC,VKE,VKE1,VIE,VIE1,VFE,
     1      VSRE,VNDSRC,KTYPRSG,VPRSG)
      ELSEIF (ITPE.EQ.95) THEN
        CALL ELEM95(VCORE,VPRNE,VPREE,VDLE,VDLEC,VKE,VKE1,VIE,VIE1,VFE,
     1      VSRE,VNDSRC,KTYPRSG,VPRSG)     
      ENDIF

      RETURN
      END
