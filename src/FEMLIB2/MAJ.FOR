      SUBROUTINE MAJ(X1,X2,N,V1,V2)
C**********************************************************************C
C MAJ.FOR                                                              C
C  02/12-1994   NTH                                                    C
C  Bruno Brouard                                                       C
C**********************************************************************C
C     EXECUTE THE VECTOR OPERATION: V2=X1*V1 + X2*V2
C       X1,X2: SCALARS     V1,V2: VECTORS
C**********************************************************************C
C  Programmed after Dhatt & Touzot, p.429                              C
C**********************************************************************C
      IMPLICIT REAL*8(A-H,O-Z)
      DIMENSION V1(1),V2(1)
C-----------------------------------------------------------------------
      DO 10 I=1,N      
 10   V2(I)=X1*V1(I)+X2*V2(I)      
      RETURN
      END    
